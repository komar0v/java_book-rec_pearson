    private void loaddata() {
        DefaultTableModel tblModel = new DefaultTableModel(new String[]{"USER ID", "AVG Rating to Book"}, 0) {

            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        };;

        try {
            System.out.println("PROCESSING...");
            connectorDb koneksi = new connectorDb();
            Connection konek = koneksi.getConnection();

            Statement stmt = konek.createStatement();
            String query = "SELECT *, AVG(book_rating) AS avg_ratings FROM tabel_rating GROUP BY user_id";
            ResultSet rs = stmt.executeQuery(query);

            while (rs.next()) {
                String user_id = rs.getString("user_id");
                String avg_rating = rs.getString("avg_ratings");
                tblModel.addRow(new Object[]{user_id, avg_rating});

            }
            stmt.close();
            konek.close();
            System.out.println("IDLE");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERR..\n" + e.getMessage());

        }
        tables1.setModel(tblModel);
    }

