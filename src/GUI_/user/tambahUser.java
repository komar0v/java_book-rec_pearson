    private void btn_simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_simpanMouseClicked

        String user_loc;
        int user_age, user_id;

        user_id = Integer.parseInt(textF_id.getText());
        user_loc = textF_loc.getText();
        user_age = Integer.parseInt(textF_age.getText());

        String regex_ = "[0-9]+";

        if (textF_age.getText().matches(regex_) && textF_id.getText().matches(regex_)) {
            if (user_loc == "" || textF_age.getText() == "" || textF_id.getText()=="") {
                JOptionPane.showMessageDialog(null, "Input Tidak Valid", "ERROR", JOptionPane.WARNING_MESSAGE);
            } else {
                try {
                    System.out.println("PROCESSING...");
                    M_User dataUser = new M_User();
                    
                    dataUser.setUser_age(user_age);
                    dataUser.setUser_id(user_id);
                    dataUser.setUser_loc(user_loc);

                    new C_User().tambah_User(dataUser);

                    JOptionPane.showMessageDialog(null, "User Ditambahkan");
                    System.out.println("IDLE");
                    this.dispose();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Input Tidak Valid", "ERROR", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btn_simpanMouseClicked
    String removeSpcialChar(String str) {
        String normalized = Normalizer.normalize(str, Normalizer.Form.NFD);
        String result = normalized.replaceAll("[^A-Za-z0-9]", " ");
        return result;
    }

